/**
 * 日付のハッシュから当日の名言を選ぶ
 */
export function getDailyQuote(date: Date = new Date()): typeof quotes[0] {
  const dateStr = date.toISOString().slice(0, 10)
  let hash = 0
  for (let i = 0; i < dateStr.length; i++) {
    hash = (hash * 31 + dateStr.charCodeAt(i)) % quotes.length
  }
  return quotes[hash]
}
